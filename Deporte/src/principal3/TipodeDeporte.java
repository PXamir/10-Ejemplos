package principal3;

/**
 *
 * @author Lenovo
 */
public class TipodeDeporte {

    private String nombre;
    private String tipo;
    
    public TipodeDeporte(){}
    
    public TipodeDeporte(String nombre, String tipo) {
        this.nombre = nombre;
        this.tipo = tipo;
    }
    
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
