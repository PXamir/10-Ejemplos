package ensamblaje_de_autos;

/**
 *
 * @author ALUMNO
 */
public class PUERTA_INFO {
    
    private String color;
    private String tipo;

    public PUERTA_INFO() {}

    public PUERTA_INFO(String color, String tipo) {
        this.color = color;
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
